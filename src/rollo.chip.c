void draw_line(chip_state_t *chip, uint32_t row, rgba_t color)
{
  uint32_t offset = chip->width * 4 * row;
  //uint8_t* c=&color;
  uint8_t *c=(uint8_t *)&color;
  for (int x = 0; x < chip->width * 4; x += 4)
  
  {
    buffer_write(chip->framebuffer, offset + x,c, sizeof(color));
  }
}

