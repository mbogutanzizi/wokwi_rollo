void chip_init()
{
  chip_state_t *chip = malloc(sizeof(chip_state_t));

  chip->dir = pin_init("DIR", INPUT);
  chip->mot = pin_init("MOT", INPUT);
  chip->esu = pin_init("ESU", OUTPUT);

  chip->eso = pin_init("ESO", OUTPUT);

  chip->c0 = pin_init("C0", INPUT);
  chip->c1 = pin_init("C1", INPUT);

  const timer_config_t timer_config = {
      .callback = chip_timer_event,
      .user_data = chip,
  };

  timer_t timer = timer_init(&timer_config);
  timer_start(timer, 100 * MS, true);

  const pin_watch_config_t config = {
      .edge = BOTH,
      .pin_change = chip_pin_change,
      .user_data = chip,
  };

  const pin_watch_config_t config1 = {
      .edge = BOTH,
      .pin_change = chip_pin_change1,
      .user_data = chip,
  };

  chip->framebuffer = framebuffer_init(&chip->width, &chip->height);

  bild = malloc(128 * 128 * 4);
  busch = malloc(128 * 29 * 4);
  wolke = malloc(47 * 47 * 4);

  GIMP_IMAGE_RUN_LENGTH_DECODE(bild, gimp_image.rle_pixel_data, 128 * 128, 4);
  GIMP_IMAGE_RUN_LENGTH_DECODE(busch, busch_image.rle_pixel_data, 128 * 29, 4);
  GIMP_IMAGE_RUN_LENGTH_DECODE(wolke, wolke_image.rle_pixel_data, 47 * 47, 4);
  for (int i = 0; i < 47 * 47; i++)
  {
    if (wolke[(i * 4)] == 70)
    {
      wolke[(i * 4)] = 0x56;
      wolke[((i) * 4 + 1)] = 0x91;
      wolke[((i) * 4 + 2)] = 0x9c;
      wolke[((i) * 4 + 3)] = 0xff;
    }
  }

  md = -1;
  if ((int)pin_read(chip->dir) > 0)
    md = 1;

  int mode = (int)pin_read(chip->c0) + (int)pin_read(chip->c1) * 2;
  ;

  pin_watch(chip->dir, &config1);
  pin_watch(chip->mot, &config);
  switch (mode)
  {
  case 0:
    value = 0;
    break;
  case 1:
    value = 128;
    break;
  case 2:

    value = 96;
    break;
  case 3:
    value = 64;
    break;
  }
}

void chip_timer_event(void *user_data)
{
  // printf("Tick\n");

  chip_state_t *chip = (chip_state_t *)user_data;
  if (m)
  {
    // printf("v %d\n",value);

    value = value + md;
    if (value < 0)
      value = 0;
    if (value > 128)
      value = 128;
  }
  if (value == 128)
  {

    pin_write(chip->esu, 1);
    pin_write(chip->eso, 0);
  }

  if (value == 0)
  {

    pin_write(chip->esu, 0);
    pin_write(chip->eso, 1);
  }
  if (value > 1 && value < 128)
  {

    pin_write(chip->esu, 0);
    pin_write(chip->eso, 0);
  }

  wx1++;
  chip->row = (chip->row + 1) % chip->height;
  rgba_t purple = {.r = 0x56, .g = 0x91, .b = 0x9c, .a = 0xff};
  rgba_t black = {.r = 0x00, .g = 0x00, .b = 0x00, .a = 0xff};
  for (int i = value; i < 127; i++)
    draw_line(chip, i, purple);
  int b = 47;
  if (128 - wx1 < 47)
    b = 128 - wx1;

  if (wx1 > 128)
    wx1 = 0;

  for (int i = 0; i < 47; i++)
  {
    buffer_write(chip->framebuffer, ((wy1 + i) * 128 + wx1) * 4, (void *)(wolke + i * 47 * 4), b * 4);
    //
  }

  buffer_write(chip->framebuffer, 128 * 4 * (128 - 29), (void *)(busch), 128 * 29 * 4);
  
  buffer_write(chip->framebuffer, 0, (void *)(bild + 128 * 128 * 4 - (128 * 4 * value)), 128 * 4 * value);
}
